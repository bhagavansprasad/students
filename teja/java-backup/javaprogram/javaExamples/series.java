package javaExamples;

public class series
{

	public static void main(String[] args)
	        {
	                int i,j,n=7;
	                for( i=1;i<=n;i++)
	                {

	                        for( j=7;j>=i;j--)
	                        {
	                                System.out.print(j);
	                        }
	                        System.out.println();
	                }
	        }

}	

	




